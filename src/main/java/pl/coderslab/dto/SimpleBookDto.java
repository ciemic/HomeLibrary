package pl.coderslab.dto;


public class SimpleBookDto {
    private String title;
    private String isbn;

    public SimpleBookDto() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }
}
